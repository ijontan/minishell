#include "minishell.h"

int	len_till_space(char *str)
{
	int	i;

	i = 0;
	while (str[i] && str[i] != ' ' && str[i] != '\'' && str[i] != '"'
		&& str[i] != '$')
		i++;
	return (i);
}
